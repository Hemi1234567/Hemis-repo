

operator = input("choose an operator (/, -, *, + ) ")


num1 = float(input("Choose your first number"))
num2 = float(input("Choose your second number"))
if operator == "+":
    result = num1 + num2
    print(result)



if operator == "/":
    result = num1 / num2
    
    print(result)
    
    
    
if operator == "-":
    result = num1 - num2
    
    print(result)
    
    
    
    
if operator == "*":
    result = num1 * num2
    
    print(result)
    
    